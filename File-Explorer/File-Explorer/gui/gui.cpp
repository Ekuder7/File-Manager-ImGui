void gui::BeginRender() noexcept
{
	MSG message;
	while (PeekMessage(&message, 0, 0, 0, PM_REMOVE))
	{
		TranslateMessage(&message);
		DispatchMessage(&message);

		if (message.message == WM_QUIT)
		{
			isRunning = !isRunning;
			return;
		}
	}

	// Start the Dear ImGui frame
	ImGui_ImplDX9_NewFrame();
	ImGui_ImplWin32_NewFrame();
	ImGui::NewFrame();
}

void gui::EndRender() noexcept
{
	ImGui::EndFrame();

	device->SetRenderState(D3DRS_ZENABLE, FALSE);
	device->SetRenderState(D3DRS_ALPHABLENDENABLE, FALSE);
	device->SetRenderState(D3DRS_SCISSORTESTENABLE, FALSE);

	device->Clear(0, 0, D3DCLEAR_TARGET | D3DCLEAR_ZBUFFER, D3DCOLOR_RGBA(0, 0, 0, 255), 1.0f, 0);

	if (device->BeginScene() >= 0)
	{
		ImGui::Render();
		ImGui_ImplDX9_RenderDrawData(ImGui::GetDrawData());
		device->EndScene();
	}

	const auto result = device->Present(0, 0, 0, 0);

	// Handle loss of D3D9 device
	if (result == D3DERR_DEVICELOST && device->TestCooperativeLevel() == D3DERR_DEVICENOTRESET)
		ResetDevice();
}

void gui::Render() noexcept
{
	ImGui::SetNextWindowPos({ 0, 0 });
	ImGui::SetNextWindowSize({ WIDTH, HEIGHT });
	ImGui::Begin(
		"Explorer",
		&isRunning,
		ImGuiWindowFlags_NoResize |
		ImGuiWindowFlags_NoSavedSettings |
		ImGuiWindowFlags_NoCollapse |
		ImGuiWindowFlags_NoMove
	);

	ImGui::SetCursorPos({ 100,30 });

	ImGui::InputText("", buffer_path, 1000);

	ImGui::SameLine();

	if (ImGui::Button("->", { 30, 19 }))
	{
		apply_path(buffer_path);
		strcpy_s(buffer_path, globals::Path.string().c_str());
	}

	ImGui::SetCursorPos({ 60,30 });

	if (ImGui::Button(".^."))
	{
		apply_path("...");
		strcpy_s(buffer_path, globals::Path.string().c_str());
	}

	static int selected;

	for (const auto& entry : fs::directory_iterator(globals::Path))
	{
		std::string type;

		if (fs::is_directory(entry)) type = "-DIR-- ";
		else if (fs::is_regular_file(entry)) type = "-FILE- ";

		if (ImGui::Selectable((type + entry.path().filename().string()).c_str(), selected, ImGuiSelectableFlags_AllowDoubleClick))
		{
			if (ImGui::IsMouseDoubleClicked(0))
			{
				apply_path(globals::Path.string() + entry.path().filename().string());
				strcpy_s(buffer_path, globals::Path.string().c_str());
			}
		}
	}

	ImGui::End();
}